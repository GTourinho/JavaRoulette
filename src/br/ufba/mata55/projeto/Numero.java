package br.ufba.mata55.projeto;

public class Numero {

	public Numero(int numero, int pos) {
		super();
		this.numero = numero;
		this.pos = pos;
	}


	private int numero, pos;

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public int getPos() {
		return pos;
	}

	public void setPos(int pos) {
		this.pos = pos;
	}
	
	
}
